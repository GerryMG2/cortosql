    public void llenarTabla() {
        DefaultTableModel tm = new DefaultTableModel() {
            public Class<?> getColumnClass(int colum) {
                switch (colum) {
                    case 0:
                        return String.class;
                    case 1:
                        return String.class;
                    case 2:
                        return String.class;
                    case 3:
                        return String.class;
                    case 4:
                        return int.class;
                    case 5:
                        return boolean.class;
                }
                return boolean.class;
            }
        };
        tm.addColumn("Nombre");
        tm.addColumn("Director");
        tm.addColumn("Pais");
        tm.addColumn("Clasificacion");
        tm.addColumn("Annio");
        tm.addColumn("en proyeccion");

        filtrodao fd = new filtrodao();
        ArrayList<filtro> filtros = fd.readall();
        for (filtro f : filtros) {
            tm.addRow(new Object[]{f.nombre, f.Director, f.pais, f.clasificacion, f.annio, f.inproyec});

        }
        jTable1.setModel(tm);
    }

    public void llenarTabla(filtro g) {
        DefaultTableModel tm = new DefaultTableModel() {
            public Class<?> getColumnClass(int colum) {
                switch (colum) {
                    case 0:
                        return String.class;
                    case 1:
                        return String.class;
                    case 2:
                        return String.class;
                    case 3:
                        return String.class;
                    case 4:
                        return int.class;
                    case 5:
                        return boolean.class;
                }
                return boolean.class;
            }
        };
        tm.addColumn("Nombre");
        tm.addColumn("Director");
        tm.addColumn("Pais");
        tm.addColumn("Clasificacion");
        tm.addColumn("Annio");
        tm.addColumn("en proyeccion");

        tm.addRow(new Object[]{g.nombre, g.Director, g.pais, g.clasificacion, g.annio, g.inproyec});

        jTable1.setModel(tm);
    }

